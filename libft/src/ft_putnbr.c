#include "libft.h"

static void	ft_putnbr_2(int nb)
{
	char	c;

	if (nb > 9)
		ft_putnbr_2(nb / 10);
	c = '0' + nb % 10;
	write(1, &c, 1);
}

void		ft_putnbr(int nb)
{
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			nb = -nb;
			write(1, "-", 1);
		}
		ft_putnbr_2(nb);
	}
}
