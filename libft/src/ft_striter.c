#include "libft.h"

void	ft_striter(char *str, void (*f)(char *))
{
	int a;

	if (!str)
		return ;
	a = 0;
	while (str[a] != '\0' && *f != NULL)
	{
		f(&str[a]);
		a++;
	}
}
