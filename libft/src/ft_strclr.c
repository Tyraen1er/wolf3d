#include "libft.h"

void	ft_strclr(char *str)
{
	int		a;

	a = 0;
	if (str != NULL)
	{
		while (str[a] != '\0')
			a++;
		while (a > 0)
		{
			a--;
			str[a] = '\0';
		}
	}
}
