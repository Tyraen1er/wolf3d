#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int		a;

	a = 0;
	while (str[a] != c && str[a] != '\0')
		a++;
	if (str[a] == c)
		return (&((char *)str)[a]);
	return (NULL);
}
