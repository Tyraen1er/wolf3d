#include "libft.h"

int	ft_atoi_hex(char *nb)
{
	int		ret;
	int		a;
	int		b;

	ret = 0;
	b = 0;
	a = 0;
	while (('a' <= nb[a] && nb[a] <= 'f') || ('A' <= nb[a] && nb[a] <= 'F') ||
			('0' <= nb[a] && nb[a] <= '9'))
		++a;
	while (a--)
	{
		if ('0' <= nb[a] && nb[a] <= '9')
			ret += (nb[a] - '0') << (b * 4);
		if ('a' <= nb[a] && nb[a] <= 'f')
			ret += (nb[a] - 'a' + 10) << (b * 4);
		if ('A' <= nb[a] && nb[a] <= 'F')
			ret += (nb[a] - 'A' + 10) << (b * 4);
		++b;
	}
	return (ret);
}
